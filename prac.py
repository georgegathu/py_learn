#STRING METHODS
food = 'Eat to Live dont Live to Eat'

print(len(food)) #len() - Count the numbers of characters in a string 
print(food.upper()) #upper() - Convert a string to upper case
print(food.lower()) #lower() - Convert a string to lower case
print(food.find('Eat')) #find() - returns the index of a character in string
print(food.replace('Eat', 'Munch')) #replace() - replace charaters and words in a string
print('Eat' in food) #Boolean expression
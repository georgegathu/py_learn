"""
Works on bits (binary numbers)
They include:
    & = bitwise AND
    | = bitwise OR
    ^ = XOR
    ~ = NOT (COMPLIMENT)
    << = LEFT SHIFT
    >> = RIGHT SHIFT
"""
# Example 1 (Using & Operator)
x = 4
y = 5

print(x & y) # Ans: 4

# Example 2 (Using | Operator)
x = 4
y = 5

print(x | y) # Ans: 5
"""
Works on bits (binary numbers)
They include:
    & = bitwise AND
    | = bitwise OR
    ^ = XOR
    ~ = NOT (COMPLIMENT)
    << = LEFT SHIFT
    >> = RIGHT SHIFT
"""
# Example 1 (Using & AND Operator)
x = 4 # 0101 (Binary)
y = 5 # 0100 (Binary)

print(x & y) # Ans: 4

# Example 2 (Using | OR Operator)
x = 4 # 0101 (Binary)
y = 5 # 0100 (Binary)

print(x | y) # Ans: 5

# Example 3 (Using ^ XOR Operator)
x = 4 # 0101 (Binary)
y = 5 # 0100 (Binary)

print(x ^ y) # Ans: 1

# Example 4 (Using ~ XOR Operator)
x = 4 # 0101 (Binary)
y = 5 # 0100 (Binary)

print(~ y) # Ans: -6